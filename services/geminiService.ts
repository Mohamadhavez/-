import { GoogleGenAI } from "@google/genai";
import { getCurrentDates } from "../utils/dateUtils";

// Initialize Gemini Client
const ai = new GoogleGenAI({ apiKey: process.env.API_KEY });

export const generateSessionReport = async (transcript: string): Promise<string> => {
  if (!transcript.trim()) return "";

  const dates = getCurrentDates();

  const prompt = `
    بصفتك سكرتيرًا تعليميًا محترفًا وبليغًا، مهمتك هي صياغة "تقرير حصة تعليمية" بناءً على الملاحظات الصوتية المفرغة أدناه.

    **الملاحظات الصوتية (النص الأصلي):**
    "${transcript}"

    **سياق الوقت الحالي (للاستئناس به إذا لم يُذكر تاريخ في النص):**
    ${dates.gregorian} | ${dates.hijri}

    ---

    **التعليمات الصارمة:**
    1. **التاريخ:** استخرج التاريخ المذكور في النص (مثلاً: "حصة أمس"، "بتاريخ 5 رمضان"). إذا لم يذكر المتحدث أي تاريخ، استخدم "سياق الوقت الحالي" المذكور أعلاه. يجب أن تكون صيغة التاريخ دقيقة وشاملة (يوم، شهر، سنة) ويفضل ذكر الهجري والميلادي إذا أمكن.
    2. **الأسلوب والصياغة:**
       - استخدم لغة عربية فصيحة، بليغة، وسهلة (السهل الممتنع).
       - تجنب الصياغة الآلية أو الركيكة. اجعل الكلام يبدو وكأنه صادر من معلم حريص ومتقن.
       - استخدم عبارات لطيفة في الختام (مثل الأمثلة).
    3. **تحديد الهوية:** حدد من السياق هل الطالب (مفرد مذكر، مفرد مؤنث، أخ، أو مجموعة/أسرة) وغير الضمائر بناءً على ذلك (الطالب، الطالبة، الأخ، الأسرة).
    4. **التنسيق:** يجب أن تلتزم **تماماً وحرفياً** بهيكل وتنسيق الأمثلة التالية (بما في ذلك الخطوط الفاصلة والرموز التعبيرية):

    **أمثلة يجب محاكاتها (Model Examples):**

    *مثال 1:*
    ━━━━━━━━━━━━━━  
    📋 *تقرير الحصّة التعليمية*  
    ━━━━━━━━━━━━━━  

    👦 *الأخ:* خالد عيسى  
    📅 *التاريخ:* الخميس ١٩ رجب ١٤٤٧هـ / ٨-١-٢٠٢٦  
    🕒 *مدة الحصة:* ساعة  
    👨‍🏫 *المعلم:* محمد حافظ  

    📖 *محتوى الحصة:*  
    - تسميع سورة *البينة*  
    - قراءة وتصحيح سورة *القدر*  
    - التدريب على *الكتابة والإملاء*  

    📌 *الواجب:*  
    - حفظ سورة *القدر* جيدًا  
    - مراجعة السور من *العصر* إلى *الناس*  
    - إعادة كتابة النص الذي كتبناه في الحصة  

    نسأل الله له التوفيق والثبات.

    *مثال 2 (للجمع):*
    ━━━━━━━━━━━━━━  
    📋 *تقرير الحصّة التعليمية*  
    ━━━━━━━━━━━━━━  

    👨‍👦 *الأسرة:* آل غلاب  
    👦 *الطلاب:* فارس وريان  
    📅 *التاريخ:* الثلاثاء ٢٤ رجب ١٤٤٧هـ  
    🕒 *مدة الحصة:* ساعة  
    👨‍🏫 *المعلّم:* (اسم المعلم إن ذكر أو يترك عاماً)

    📖 *محتوى الحصّة:*  
    🔹 *فارس:*  
    - تسميع من سورة *العلق* إلى *البينة*  
    📌 *واجب فارس:*  
    مراجعة من سورة *الزلزلة* إلى *الهمزة*  

    🔹 *ريان:*  
    - تسميع سورة *العلق* إلى الآية 10  
    📌 *واجب ريان:*  
    - حفظ السور المطلوبة  

    بارك الله فيكم وفي متابعتكم.

    ---
    **ملاحظات هامة للمولد:**
    - إذا لم يذكر اسم المعلم في النص، اترك خانة المعلم فارغة أو اكتب "معلم المادة".
    - استنبط مدة الحصة من النص، إذا لم تذكر لا تضعها أو قدرها بناء على الكمية (تقديرياً).
    - لا تضف أي مقدمات قبل الخط الفاصل الأول، ولا أي تعليقات بعد دعاء الختام.
    - التزم بوضع النجوم * حول الكلمات الهامة والعناوين ليظهر الخط عريضاً في واتساب.
  `;

  try {
    const response = await ai.models.generateContent({
      model: 'gemini-3-flash-preview',
      contents: prompt,
      config: {
        temperature: 0.4, // Lower temperature for more consistent formatting
      }
    });

    return response.text || "عذراً، لم أتمكن من إنشاء التقرير. يرجى المحاولة مرة أخرى.";
  } catch (error) {
    console.error("Gemini API Error:", error);
    throw new Error("حدث خطأ أثناء الاتصال بالذكاء الاصطناعي.");
  }
};